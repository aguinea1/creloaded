void	ft_putchar(char c);

void	ft_is_negative(int n)
{
	char	l;
	char	t;

	l = 'N';
	t = 'P';
	if (n < 0)
	{
		ft_putchar(l);
	}
	else
	{
		ft_putchar(t);
	}
}
